    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        if(productorSeleccionado == null){
                llenarComboboxProductor();
        }else{
           DefaultComboBoxModel<String> modelProductor = new DefaultComboBoxModel<>();
           var productorStr = productorSeleccionado.getId()+ " "+productorSeleccionado.getNombre();
            modelProductor.addElement(productorStr);
            comboBoxProductor.setModel(modelProductor);
        }
       // llenarComboBoxConstituyentes();
    }//GEN-LAST:event_formWindowOpened

    private void llenarComboboxProductor(){
        DefaultComboBoxModel<String> modelProductor = new DefaultComboBoxModel<>();
        ProductorDao productorDao = new ProductorDao();
        ListarProductoresCasoDeUso casoDeUso = new ListarProductoresCasoDeUso(productorDao);
        ListarProductorControlador controlador = new ListarProductorControlador(casoDeUso);
        
        ListarProductorResponseDto response = controlador.ejecutarAccion();
        
        for (var productor : response.getProductores()){
            var productorStr = productor.getId()+ " "+productor.getNombre();
            modelProductor.addElement(productorStr);
        }
        comboBoxProductor.setModel(modelProductor);
    }
