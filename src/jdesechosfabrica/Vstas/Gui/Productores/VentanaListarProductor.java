    private void cargarDatosProductor() {
        DefaultTableModel modelProductor = new DefaultTableModel(new Object[]{
            "ID",
            "Nombre",
            "Tipo Actividad",
            "Pais",
            "Ciudad",
            "Barrio",
            "Calle",
            "Numero"
        }, 0) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        ProductorDao productorDao = new ProductorDao();
        ListarProductoresCasoDeUso casoDeUso = new ListarProductoresCasoDeUso(productorDao);
        ListarProductorControlador controlador = new ListarProductorControlador(casoDeUso);

        response = controlador.ejecutarAccion();

        for (var productorDto : response.getProductores()) {
            modelProductor.addRow(
                    new Object[]{
                        productorDto.getId(),
                        productorDto.getNombre(),
                        productorDto.getTipoActividad(),
                        productorDto.getPais(),
                        productorDto.getCiudad(),
                        productorDto.getBarrio(),
                        productorDto.getCalle(),
                        productorDto.getNumero(),}
            );
        }

        tableProductores.setModel(modelProductor);

        var selectionModel = tableProductores.getSelectionModel();

        selectionModel.addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int filaSeleccionada = tableProductores.getSelectedRow();

                    llenarTablaResiduos(response.getProductores().get(filaSeleccionada));
                }
            }

        });
    }

    private void llenarTablaResiduos(ProductorDto productor) {
        DefaultTableModel modelResiduos = new DefaultTableModel(new Object[]{
            "ID",
            "CÃ³digo",
            "Tipo de Residuo",
            "Fecha de Generacion",
            "Cantidad Total Kg",
            "Hubo incidente de seguridad"
        }, 0) {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        try {
            ResiduoDao residuoDao = new ResiduoDao();
            ObtenerResiduoPorIdCasoDeUso casoDeUso = new ObtenerResiduoPorIdCasoDeUso(residuoDao);
            OtenerResiduoPorIdControlador controlador = new OtenerResiduoPorIdControlador(casoDeUso);

            for (var id : productor.getResiduosIds()) {
                ObtenerResiduoPorIdRespuestaDto respuesta = controlador.ejecutarAccion(new ObtenerResiduoPorIdPeticionDto(id));
                modelResiduos.addRow(new Object[]{
                    respuesta.getId(),
                    respuesta.getCodigo(),
                    respuesta.getTipoResiduo(),
                    respuesta.getFechaGeneracion(),
                    respuesta.getCantidadTotalKilos(),
                    respuesta.isHuboIncidenteSeguridad()
                });
            }

            tableResiduosProductor.setModel(modelResiduos);
        } catch (Exception error) {
            JOptionPane.showMessageDialog(this, error.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);

        }
    }

